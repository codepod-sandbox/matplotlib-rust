class TestBboxCreation:
    def test_from_bounds(self):
        bb = Bbox.from_bounds(1, 2, 3, 4)
        assert bb.x0 == 1
        assert bb.y0 == 2
        assert bb.width == 3
        assert bb.height == 4
        assert bb.x1 == 4
        assert bb.y1 == 6

    def test_from_extents(self):
        bb = Bbox.from_extents(1, 2, 4, 6)
        assert bb.x0 == 1
        assert bb.y0 == 2
        assert bb.x1 == 4
        assert bb.y1 == 6

    def test_unit(self):
        bb = Bbox.unit()
        assert bb.x0 == 0
        assert bb.y0 == 0
        assert bb.x1 == 1
        assert bb.y1 == 1

    def test_null(self):
        bb = Bbox.null()
        assert bb.x0 == float('inf')
        assert bb.y0 == float('inf')
        assert bb.x1 == float('-inf')
        assert bb.y1 == float('-inf')

    def test_default_constructor(self):
        bb = Bbox()
        assert bb.x0 == 0.0
        assert bb.y0 == 0.0
        assert bb.x1 == 1.0
        assert bb.y1 == 1.0

    def test_points_constructor(self):
        bb = Bbox([[1, 2], [3, 4]])
        assert bb.x0 == 1.0
        assert bb.y0 == 2.0
        assert bb.x1 == 3.0
        assert bb.y1 == 4.0

    def test_points_as_float(self):
        bb = Bbox([[1, 2], [3, 4]])
        assert isinstance(bb.x0, float)
        assert isinstance(bb.y0, float)


