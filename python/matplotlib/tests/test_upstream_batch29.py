class TestColorUtilitiesParametric:
    """Parametric tests for color conversion utilities."""

    @pytest.mark.parametrize('color,expected_rgb', [
        ('red', (1.0, 0.0, 0.0)),
        ('green', (0.0, 0.5019607843137255, 0.0)),
        ('blue', (0.0, 0.0, 1.0)),
        ('black', (0.0, 0.0, 0.0)),
        ('white', (1.0, 1.0, 1.0)),
        ('yellow', (1.0, 1.0, 0.0)),
        ('cyan', (0.0, 1.0, 1.0)),
        ('magenta', (1.0, 0.0, 1.0)),
    ])
    def test_named_color_to_rgba(self, color, expected_rgb):
        """Named colors convert to correct RGBA."""
        rgba = mcolors.to_rgba(color)
        assert abs(rgba[0] - expected_rgb[0]) < 1e-3
        assert abs(rgba[1] - expected_rgb[1]) < 1e-3
        assert abs(rgba[2] - expected_rgb[2]) < 1e-3
        assert rgba[3] == 1.0  # fully opaque

    @pytest.mark.parametrize('hex_color', [
        '#ff0000', '#00ff00', '#0000ff', '#ffffff', '#000000',
        '#abcdef', '#123456'
    ])
    def test_hex_color_to_rgba(self, hex_color):
        """Hex colors convert to RGBA tuples."""
        rgba = mcolors.to_rgba(hex_color)
        assert len(rgba) == 4
        assert all(0.0 <= v <= 1.0 for v in rgba)

    @pytest.mark.parametrize('short', ['r', 'g', 'b', 'k', 'w', 'y', 'c', 'm'])
    def test_short_color_names(self, short):
        """Short color names convert to RGBA."""
        rgba = mcolors.to_rgba(short)
        assert len(rgba) == 4
        assert all(0.0 <= v <= 1.0 for v in rgba)

    @pytest.mark.parametrize('alpha', [0.0, 0.25, 0.5, 0.75, 1.0])
    def test_rgba_alpha_channel(self, alpha):
        """to_rgba preserves specified alpha."""
        rgba = mcolors.to_rgba('red', alpha=alpha)
        assert abs(rgba[3] - alpha) < 1e-10

    @pytest.mark.parametrize('color', ['red', '#ff0000', 'r', (1, 0, 0)])
    def test_to_hex_roundtrip(self, color):
        """to_hex converts color to hex string."""
        hex_color = mcolors.to_hex(color)
        assert hex_color.startswith('#')
        assert len(hex_color) == 7  # #RRGGBB

    def test_red_rgba_components(self):
        """Red has correct RGBA: (1, 0, 0, 1)."""
        rgba = mcolors.to_rgba('red')
        assert abs(rgba[0] - 1.0) < 1e-5
        assert abs(rgba[1] - 0.0) < 1e-5
        assert abs(rgba[2] - 0.0) < 1e-5

    def test_white_rgba_components(self):
        """White has correct RGBA: (1, 1, 1, 1)."""
        rgba = mcolors.to_rgba('white')
        assert abs(rgba[0] - 1.0) < 1e-5
