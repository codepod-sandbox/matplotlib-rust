class TestNormParametric:
    """Parametric tests for Normalize."""

    @pytest.mark.parametrize('vmin,vmax,val,expected', [
        (0, 1, 0.5, 0.5),
        (0, 10, 5, 0.5),
        (0, 100, 25, 0.25),
        (-1, 1, 0, 0.5),
        (-10, 10, 5, 0.75),
    ])
    def test_normalize_value(self, vmin, vmax, val, expected):
        """Normalize maps values to [0, 1]."""
        norm = mcolors.Normalize(vmin=vmin, vmax=vmax)
        result = float(norm(val))
        assert abs(result - expected) < 1e-10

    @pytest.mark.parametrize('vmin,vmax', [
        (0, 1), (-1, 1), (0, 100), (-50, 50)
    ])
    def test_normalize_vmin_maps_to_zero(self, vmin, vmax):
        """Normalize maps vmin to 0."""
        norm = mcolors.Normalize(vmin=vmin, vmax=vmax)
        assert abs(float(norm(vmin))) < 1e-10

    @pytest.mark.parametrize('vmin,vmax', [
        (0, 1), (-1, 1), (0, 100), (-50, 50)
    ])
    def test_normalize_vmax_maps_to_one(self, vmin, vmax):
        """Normalize maps vmax to 1."""
        norm = mcolors.Normalize(vmin=vmin, vmax=vmax)
        assert abs(float(norm(vmax)) - 1.0) < 1e-10

    def test_normalize_clips_below(self):
        """Normalize clips values below vmin to 0."""
        norm = mcolors.Normalize(vmin=0, vmax=1, clip=True)
        assert float(norm(-0.5)) == 0.0

    def test_normalize_clips_above(self):
        """Normalize clips values above vmax to 1."""
        norm = mcolors.Normalize(vmin=0, vmax=1, clip=True)
        assert float(norm(1.5)) == 1.0

    def test_normalize_array(self):
        """Normalize works on arrays."""
        norm = mcolors.Normalize(vmin=0, vmax=10)
        arr = np.array([0.0, 5.0, 10.0])
        result = norm(arr)
        np.testing.assert_allclose(result, [0.0, 0.5, 1.0], atol=1e-10)


