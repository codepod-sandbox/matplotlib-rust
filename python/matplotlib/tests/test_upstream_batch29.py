class TestFigureParametric:
    """Parametric tests for Figure properties."""

    @pytest.mark.parametrize('w,h', [
        (4, 3), (6.4, 4.8), (8, 6), (10, 10), (12, 4)
    ])
    def test_figure_size(self, w, h):
        """Figure size is stored correctly."""
        fig = plt.figure(figsize=(w, h))
        assert abs(fig.get_figwidth() - w) < 1e-10
        assert abs(fig.get_figheight() - h) < 1e-10
        plt.close('all')

    @pytest.mark.parametrize('dpi', [72, 96, 100, 150, 200, 300])
    def test_figure_dpi(self, dpi):
        """Figure DPI is stored correctly."""
        fig = plt.figure(dpi=dpi)
        assert fig.get_dpi() == dpi
        plt.close('all')

    @pytest.mark.parametrize('n_axes', [1, 2, 3, 4, 6])
    def test_figure_axes_count(self, n_axes):
        """Figure has correct number of axes."""
        fig = plt.figure()
        for i in range(n_axes):
            fig.add_subplot(1, n_axes, i + 1)
        assert len(fig.get_axes()) == n_axes
        plt.close('all')

    @pytest.mark.parametrize('suptitle', ['Main Title', 'Figure Overview', 'My Plot'])
    def test_figure_suptitle(self, suptitle):
        """Figure suptitle is stored."""
        fig = plt.figure()
        fig.suptitle(suptitle)
        assert fig.get_suptitle() == suptitle
        plt.close('all')

    def test_figure_clf_removes_axes(self):
        """clf() removes all axes from figure."""
        fig = plt.figure()
        fig.add_subplot(1, 2, 1)
        fig.add_subplot(1, 2, 2)
        fig.clf()
        assert len(fig.get_axes()) == 0
        plt.close('all')

    def test_figure_add_subplot_returns_axes(self):
        """add_subplot returns an Axes."""
        from matplotlib.axes import Axes
        fig = plt.figure()
        ax = fig.add_subplot(1, 1, 1)
        assert isinstance(ax, Axes)
        plt.close('all')


