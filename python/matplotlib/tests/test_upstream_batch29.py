class TestColormapParametric:
    """Parametric tests for colormaps."""

    @pytest.mark.parametrize('name', [
        'viridis', 'plasma', 'inferno', 'magma', 'cividis',
        'gray', 'hot', 'cool', 'jet', 'rainbow',
    ])
    def test_colormap_exists(self, name):
        """Standard colormap names are registered."""
        cmap = cm.get_cmap(name)
        assert cmap is not None

    @pytest.mark.parametrize('name', ['viridis', 'plasma', 'gray', 'hot'])
    def test_colormap_name(self, name):
        """Colormap has correct name attribute."""
        cmap = cm.get_cmap(name)
        assert cmap.name == name

    @pytest.mark.parametrize('val', [0.0, 0.25, 0.5, 0.75, 1.0])
    def test_colormap_returns_rgba(self, val):
        """Colormap returns RGBA tuple for any value in [0,1]."""
        cmap = cm.get_cmap('viridis')
        rgba = cmap(val)
        assert len(rgba) == 4
        assert all(0.0 <= v <= 1.0 for v in rgba)

    def test_colormap_zero_and_one_differ(self):
        """Colormap value at 0 differs from value at 1."""
        cmap = cm.get_cmap('viridis')
        rgba0 = cmap(0.0)
        rgba1 = cmap(1.0)
        assert rgba0 != rgba1

    def test_colormap_reversed(self):
        """Reversed colormap has _r suffix and maps values inversely."""
        cmap = cm.get_cmap('viridis')
        cmap_r = cm.get_cmap('viridis_r')
        assert cmap_r is not None
        # viridis(0) should approximately equal viridis_r(1)
        rgba_orig_0 = cmap(0.0)
        rgba_rev_1 = cmap_r(1.0)
        for a, b in zip(rgba_orig_0[:3], rgba_rev_1[:3]):
            assert abs(a - b) < 0.01

    @pytest.mark.parametrize('n', [2, 4, 8, 16, 256])
    def test_colormap_array_length(self, n):
        """Colormap applied to n values returns n colors."""
        cmap = cm.get_cmap('viridis')
        vals = np.linspace(0, 1, n)
        colors = cmap(vals)
        assert colors.shape[0] == n
        assert colors.shape[1] == 4


