        a = Artist()
        a.set_alpha(0.0)
        assert a.get_alpha() == 0.0
        a.set_alpha(1.0)
        assert a.get_alpha() == 1.0
        a.set_alpha(0.5)
        assert a.get_alpha() == 0.5

